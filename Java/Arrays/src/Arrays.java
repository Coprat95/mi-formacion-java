public class Arrays {
    public static void main(String[] args) {
        // Arreglos
        // 1. Declaramos un arreglo
        int[] enteros; // normalmente los nombres van en plural ya que son varios elementos.
        // 2. Inicializar el arreglo ( reservar espacio memoria)
        enteros = new int[3]; // se crea índice 0,1,2 en el heap de memoria

        // Declarar e inicializar un arreglo en una sola línea
        //int [] numeros = new int[2]; // índice 0 y 1
        var numeros = new int[2];
    }
}
